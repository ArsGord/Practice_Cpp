// 1. а) Сумма квадратов синусов от x.cpp : Этот файл содержит функцию "main". Здесь начинается и заканчивается выполнение программы.
//

#include <iostream>
using namespace std;

int main()
{
    setlocale(LC_ALL, "RUS"); // Добавление русской локали
    cout << "Введите n:\n";
    int n;
    cin >> n; // Ввод значения n с клавиатуры
    cout << "Введите x:\n";
    float x;
    cin >> x; // Ввод значения x с клавиатуры

    float m = 1, s = 0; // m - слагаемое, s - сумма
    for (int i = 1; i <= n; i++) {
        m = m * sin(x); // Записываем в переменную m произведение предыдущего члена ряда и sin(x)
        s = s + m; // Подсчитываем сумму ряда
    }
    cout << "Ответ:\n" << s; // Выводим ответ
    return 0;
}

